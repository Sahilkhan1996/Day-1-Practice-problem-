package com.bridgelabz.defaultValue;

public class DefaultValueDisplayer {
	static byte a;
	static short b;
	static int c;
	static long d;
	static char e;
	static float f;
	static double g;
	static boolean h;
	
public static void main(String[] args) {
	
	System.out.println("Default value of byte: "+a);
	System.out.println("Default value of short: "+b);
	System.out.println("Default value of int: "+c);
	System.out.println("Default value of long: "+d);
	System.out.println("Default value of char: "+e);
	System.out.println("Default value of float: "+f);
	System.out.println("Default value of double: "+g);
	System.out.println("Default value of boolean: "+h);
}
}
